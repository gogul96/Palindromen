package com.solution.palindromen.utils;

public class ReversalOutput {

    private String content;
    private String timestamp;
    private Long longest_timestamp_string;

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(String timestamp) {
        this.timestamp = timestamp;
    }

    public Long getLongest_timestamp_string() {
        return longest_timestamp_string;
    }

    public void setLongest_timestamp_string(Long longest_timestamp_string) {
        this.longest_timestamp_string = longest_timestamp_string;
    }
}
